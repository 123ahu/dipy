greeting = "Hello World"
device = "COMPUTER"

print(greeting)
print(device)

# Accessing an element in a string
print(greeting[0])
print(greeting[1])

# Determining size of a string
print(len(greeting))

# Modifying a string
print(greeting.upper())
print(device.lower())

# String concatenation (joining strings)
Firstname = "Denzel "
Lastname = "Delacruz"

print(Firstname + " " + Lastname)
print(Firstname + Lastname)

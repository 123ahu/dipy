courses = ["Artificial Intelligence", "Computer Science", "Software Engineering", "BIT"]
print(courses)

# Accessing an element in an array
print(courses[3])

# Looping through an array
for course in courses:
    print(course)

# Adding data to an array
courses.append("Data Science")
print(courses)

# Deleting data from an array
courses.remove("BIT")
print(courses)

